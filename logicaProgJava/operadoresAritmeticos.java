import java.util.*;

public class operadoresAritmeticos {
    public static void main(String[] args) {
        double numA, numB, total;

        numA = 8;
        numB = 3;

        total = numA + numB;
        System.out.println(total);

        total = numA * numB;
        System.out.println(total);

        total = numA % numB;
        System.out.println(total);
    }
}