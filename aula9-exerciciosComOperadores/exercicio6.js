// Para fazer uma promoção, os comerciantes estão procurando aumentoar suas vendas oferecendo descontos. Faça um algoritmo que possa receber um valor de um produto e que escreva o novo valor tendo em vista que o desconto foi de 9%.
let produto = 1000;
let desconto = produto * 0.09;
let novoValor = produto - desconto;

console.log(`O novo valor é de: R$ ${novoValor} reais.`);